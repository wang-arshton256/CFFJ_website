                            <h3 class="pt-4 pb-4"><strong>The Church and Family</strong></h3>

                            <li><i class="bi bi-check2-circle"></i>The Church should reclaim and
                                continually re-affirm the honorable calling to enable men and women
                                to enter holy matrimony, to form holy and consecrated households,
                                and for those households to be formed into the larger Body of
                                Christ, the people of God, the Bride of Christ.</li>


                            <li><i class="bi bi-check2-circle"></i>The Church and other religious
                                organizations should solidify their roles in the families as
                                counselors and exhort the hopeless, instill Christian values, pray
                                for the sick, take care of the orphans and widows as Christ called
                                us to do. This should be done with a mission of unifying families,
                                caring for the broken and sharing the love of God.</li>


                            <li><i class="bi bi-check2-circle"></i>The Church should also encourage
                                the family to reach out to their communities, have proper health
                                practices, and take care of their environment for generations to
                                come.</li>



                            <h3 class="pt-4 pb-4"><strong>The Church and the Economy</strong>
                            </h3>
                            <li><i class="bi bi-check2-circle pt-3"></i>The Church should work
                                together with the government and other institutions in creating
                                public policies that benefit their congregation as well as be
                                positive influencers when it comes to policy implementation.</li>

                            <li><i class="bi bi-check2-circle"></i>The Church should inform the
                                communities it serves on any policies that benefit them, how to
                                benefit from available opportunities, and how to be innovative among
                                others via its notice boards and announcements. This is because the
                                Church can easily communicate to its communities in different
                                languages and in the context they can understand. </li>





                        </ul>








