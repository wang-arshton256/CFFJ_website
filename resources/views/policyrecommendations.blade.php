                                organizations should instill ethical leadership values and standards
                                amongst its congregation and Nation by first being good examples,
                                sensitizing communities on their call as leaders under God and
                                rewarding / recognizing great leaders. </li>


                            <ul>
                                <li><i class="bi bi-check2-circle"></i> TThe Church and other
                                    religious organizations should hold regular leadership training
                                    and
                                    workshops to engage business and government leaders on ethical
                                    leadership and its impact on the economy and citizens.</li>



