                    <div class="section-title pt-4">

                        <h2><strong> Justice</strong></h2>
                        <p>For several years, Uganda has grappled with corruption, poor public
                            performance, cultural injustice and among <br> others, and
                            it is the responsibility of every Ugandan and the Judicial system at
                            large to ensure that<br> justice prevails, and crime is punished. We
                            believe that the following issues should be<br> tackled on the road to
                            building justice in Uganda.</p>

                    </div>



                    <!-- =======End Sub Section Title ======= -->

                    <div class="row">
                        <div class="col-lg-6 pt-4">
                            <img src="assets/img/policyrecommendations/justice.png"
